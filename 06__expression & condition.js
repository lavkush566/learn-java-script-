console.log("a++",a++); // it will give output as 11 because it will first print the value of a and then increment it
console.log("--a",--a); // it will give output as 11 because it will first decrement the value of a and then print it
console.log("a--",a--); // it will give output as 11 because it will first print the value of a and then decrement it

